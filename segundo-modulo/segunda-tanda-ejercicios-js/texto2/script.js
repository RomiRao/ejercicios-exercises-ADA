//Funcion COMPETENCIA

const obtenerCompetencia = (a, b) => {
    console.log(`${a} vs ${b}`)
};

obtenerCompetencia('JavaScript', 'Python'); // `JavaScript vs. Python`
obtenerCompetencia('Coca', 'Pepsi') // `Coca vs. Pepsi`
obtenerCompetencia('Perros', 'Gatos') // `Perros vs. Gatos`

//Funcion EMAIL

const generarEmail = (usuario, dominio) => {
    console.log(`${usuario}@${dominio}.com`)
};

generarEmail('adalovelace', 'gmail') // 'adalovelace@gmail.com'

